const { nanoid } = require('nanoid');
const books = require('./books');
const { convertBool } = require('./fn');

// Fungsi untuk menghandle request POST book
const addBook = (request, h) => {
  const {
    name, year, author, summary, publisher, pageCount, readPage, reading,
  } = request.payload;

  if (!name) {
    const response = h.response({
      status: 'fail',
      message: 'Gagal menambahkan buku. Mohon isi nama buku',
    });
    response.code(400);
    return response;
  }

  if (readPage > pageCount) {
    const response = h.response({
      status: 'fail',
      message: 'Gagal menambahkan buku. readPage tidak boleh lebih besar dari pageCount',
    });
    response.code(400);
    return response;
  }

  const id = nanoid(16);
  const finished = pageCount === readPage;
  const insertedAt = new Date().toISOString();
  const updatedAt = insertedAt;

  const newBook = {
    id,
    name,
    year,
    author,
    summary,
    publisher,
    pageCount,
    readPage,
    finished,
    reading,
    insertedAt,
    updatedAt,
  };

  books.push(newBook);

  const isSuccess = books.filter((book) => book.id === id).length === 1;

  if (isSuccess) {
    const response = h.response({
      status: 'success',
      message: 'Buku berhasil ditambahkan',
      data: {
        bookId: id,
      },
    });
    response.code(201);
    return response;
  }

  const response = h.response({
    status: 'fail',
    message: 'Buku gagal ditambahkan',
  });
  response.code(500);
  return response;
};
// End addBook function

// Fungsi untuk menghandle request GET all books
const getBooks = (request, h) => {
  const { name, reading, finished } = request.query;
  let bookFound = books;

  if (name) {
    bookFound = books.filter((book) => (book.name.toLowerCase()).includes(name.toLowerCase()));
  }
  /* Informasi:
    Untuk menghasilkan boolean true false dari reading dan finished query
    yang awalnya memiliki nilai 1 aatau 0.
    funsi convertBool berasal dari file fn.js
  */
  if (reading !== undefined) {
    bookFound = bookFound.filter((book) => book.reading === convertBool(parseInt(reading, 10)));
  }
  if (finished !== undefined) {
    bookFound = bookFound.filter((book) => book.finished === convertBool(parseInt(finished, 10)));
  }

  const bookResult = bookFound.map((book) => {
    const { id, publisher } = book;
    const nm = book.name;
    return { id, name: nm, publisher };
  });
  const response = h.response({
    status: 'success',
    data: {
      books: bookResult,
    },
  });
  return response;
};
// End getBooks function

// Fungsi untuk menghandle request GET book specified
const getSpecifiedBook = (request, h) => {
  const { bookId } = request.params;

  const book = books.filter((n) => n.id === bookId)[0];

  if (book) {
    return {
      status: 'success',
      data: {
        book,
      },
    };
  }
  const response = h.response({
    status: 'fail',
    message: 'Buku tidak ditemukan',
  });
  response.code(404);
  return response;
};
// End getSpecifiedBook Function

// Function untuk handle request PUT book
const editBook = (request, h) => {
  const { bookId } = request.params;

  const {
    name, year, author, summary, publisher, pageCount, readPage, reading,
  } = request.payload;

  if (!name) {
    const response = h.response({
      status: 'fail',
      message: 'Gagal memperbarui buku. Mohon isi nama buku',
    });
    response.code(400);
    return response;
  }

  if (readPage > pageCount) {
    const response = h.response({
      status: 'fail',
      message: 'Gagal memperbarui buku. readPage tidak boleh lebih besar dari pageCount',
    });
    response.code(400);
    return response;
  }

  const updatedAt = new Date().toISOString();
  const index = books.findIndex((book) => book.id === bookId);

  if (index !== -1) {
    books[index] = {
      ...books[index],
      name,
      year,
      author,
      summary,
      publisher,
      pageCount,
      readPage,
      reading,
      updatedAt,
    };

    const response = h.response({
      status: 'success',
      message: 'Buku berhasil diperbarui',
    });
    response.code(200);
    return response;
  }

  const response = h.response({
    status: 'fail',
    message: 'Gagal memperbarui buku. Id tidak ditemukan',
  });
  response.code(404);
  return response;
};
// End editBook function

// Function untuk menghandle request DELETE book
const deleteBook = (request, h) => {
  const { bookId } = request.params;
  const index = books.findIndex((book) => book.id === bookId);

  if (index !== -1) {
    books.splice(index, 1);
    const response = h.response({
      status: 'success',
      message: 'Buku berhasil dihapus',
    });
    response.code(200);
    return response;
  }

  const response = h.response({
    status: 'fail',
    message: 'Buku gagal dihapus. Id tidak ditemukan',
  });
  response.code(404);
  return response;
};
// End deletebook function

module.exports = {
  addBook, getBooks, getSpecifiedBook, editBook, deleteBook,
};
